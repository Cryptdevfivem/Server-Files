GMAConfig = {} -- Global variable for easy referencing. 

GMAConfig.MoneyUiEnabled = false; -- Set to false to disable Money in the top right corner. 
GMAConfig.SurvivalUiEnabled = false; -- Controls the UI under the healthbar.
GMAConfig.EnableComa = true; -- Controls the GMA coma on death.
GMAConfig.EnableFoodAndWater = false; -- Controls the food and water system.
GMAConfig.EnableHealthRegen = true; -- Controls the health regen. (Whether they regen health after taking damage do not disable if coma is enabled.)
GMAConfig.EnableBuyVehicles = false; -- Enables ability to buy vehicles from the RageUI Garages.  
GMAConfig.LoadPreviews = true; -- Controls the car previews with the RageUI Garages.
GMAConfig.VehicleStoreRadius = 250; -- Controls radius a vehicle can be stored from.
GMAConfig.AdminCoolDown = true; -- Enables an admin cooldown on call admin.
GMAConfig.AdminCooldownTime = 60; -- 1 minute in (seconds) duration of cooldown. 
GMAConfig.StoreWeaponsOnDeath = true; -- Stores the players weapon on death allowing them to be looted.
GMAConfig.DoNotDisplayIps = false; -- Removes all GMA related references in the console to player ip addresses.
GMAConfig.LoseItemsOnDeath = true; -- Controls whether you lose inventory items on death.
GMAConfig.AllowMoreThenOneCar = true; -- Controls if you can have more than one car out.
GMAConfig.F10System = true; -- Logs warnings and can be accessed via F10 (Thanks to Rubbertoe98) (https://github.com/rubbertoe98/FiveM-Scripts/tree/master/GMA_punishments)
GMAConfig.ServerName = "GMA" -- Controls the name that is displayed on warnings title etc.
GMAConfig.PlayerSavingTime = 3000 -- Time in milliseconds to update Player saving
---------------
GMAConfig.LootBags = true; -- Enables loot bags and disables looting. 
GMAConfig.DisplayNamelLootbag = true; -- Enables notification of who's lootbag you have opened
